    // ── COUNTER ANIMATION ──────────────────────────────────────
    function animateCounter(el, target) {
      let current = 0;
      const step = target / 40;
      const timer = setInterval(() => {
        current += step;
        if (current >= target) { el.textContent = target + (el.dataset.suffix || ''); clearInterval(timer); }
        else el.textContent = Math.floor(current) + (el.dataset.suffix || '');
      }, 40);
    }

    const statObserver = new IntersectionObserver((entries) => {
      entries.forEach(e => {
        if (e.isIntersecting && !e.target.dataset.animated) {
          e.target.dataset.animated = 'true';
          document.querySelectorAll('.stat-value').forEach(sv => {
            const raw = parseInt(sv.textContent);
            const suffix = sv.textContent.replace(/[0-9]/g, '');
            sv.dataset.suffix = suffix;
            if (!isNaN(raw)) animateCounter(sv, raw);
          });
        }
      });
    }, { threshold: 0.5 });

    const statsSection = document.querySelector('.hero-stats');
